# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

def run_verification(ctx: GeneratorContext) -> None:
    """Run all architecture verification rules."""
    print("\n=== VERIFICATION ===\n")

    gateway_routes_dir = ctx.resolve(f"{GATEWAY_BASE}/routes/curriculum")

    # Rule 1: No direct model imports in curriculum routes
    if gateway_routes_dir.exists():
        for f in gateway_routes_dir.glob("*.routes.ts"):
            content = f.read_text()
            has_model_import = "from '../models'" in content or "from '../../models'" in content
            ctx.verify_rule(
                f"No direct model import in {f.name}",
                not has_model_import,
                f"Found direct model import" if has_model_import else "",
            )
    else:
        ctx.warn("Routes directory does not exist yet — skipping model import check")

    # Rule 2: No inline JWT in curriculum routes
    if gateway_routes_dir.exists():
        for f in gateway_routes_dir.glob("*.routes.ts"):
            content = f.read_text()
            has_jwt = "jwt.verify" in content or "jsonwebtoken" in content
            ctx.verify_rule(
                f"No inline JWT in {f.name}",
                not has_jwt,
                f"Found inline JWT" if has_jwt else "",
            )

    # Rule 3: All generated files have banner
    for rel_path, _ in ctx.files_generated:
        full = ctx.resolve(rel_path)
        if full.exists() and full.suffix in ('.ts', '.tsx'):
            content = full.read_text()
            has_banner = GENERATED_BANNER.split("—")[0].strip() in content
            ctx.verify_rule(
                f"Generated banner in {rel_path}",
                has_banner,
                "Missing generated banner" if not has_banner else "",
            )

    # Rule 4: All migration files have rollback comment
    migrations_dir = ctx.resolve(MIGRATIONS_BASE)
    if migrations_dir.exists():
        for f in migrations_dir.glob("*.sql"):
            content = f.read_text()
            has_rollback = "ROLLBACK SQL:" in content or "DROP TABLE" in content
            ctx.verify_rule(
                f"Rollback SQL in {f.name}",
                has_rollback,
                "Missing rollback SQL comment" if not has_rollback else "",
            )

    # Rule 5: Check master router mounts all sub-routers
    index_path = ctx.resolve(f"{GATEWAY_BASE}/routes/curriculum/index.ts")
    if index_path.exists():
        content = index_path.read_text()
        for mod in ROUTE_MODULES:
            has_mount = f"{mod}Router" in content
            ctx.verify_rule(
                f"Router '{mod}' mounted in index",
                has_mount,
                f"Missing mount for {mod}" if not has_mount else "",
            )

    # Rule 6: All adapters use CircuitBreaker
    adapters_dir = ctx.resolve(f"{GATEWAY_BASE}/adapters")
    if adapters_dir.exists():
        for f in adapters_dir.glob("*.adapter.ts"):
            content = f.read_text()
            has_breaker = "CircuitBreaker" in content
            ctx.verify_rule(
                f"Circuit breaker in {f.name}",
                has_breaker,
                "Missing CircuitBreaker" if not has_breaker else "",
            )

    # Rule 7: Auth middleware index exports all required functions
    auth_index = ctx.resolve(f"{GATEWAY_BASE}/middleware/auth/index.ts")
    if auth_index.exists():
        content = auth_index.read_text()
        for fn in ['requireAuth', 'resolveTenant', 'requireScope', 'requireInstitutionAccess']:
            ctx.verify_rule(
                f"Auth index exports {fn}",
                fn in content,
                f"Missing export: {fn}" if fn not in content else "",
            )

    # Summary
    print(f"\n=== SUMMARY ===")
    print(f"  Files generated: {ctx.stats['created']}")
    print(f"  Files backed up: {ctx.stats['backed_up']}")
    print(f"  Verification errors: {len(ctx.verification_errors)}")
    print(f"  Verification warnings: {len(ctx.verification_warnings)}")

    if ctx.verification_errors:
        print(f"\n  ERRORS:")
        for e in ctx.verification_errors:
            print(f"    ✗ {e}")

    if ctx.verification_warnings:
        print(f"\n  WARNINGS:")
        for w in ctx.verification_warnings:
            print(f"    ⚠ {w}")


