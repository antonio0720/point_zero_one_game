    files = []
    files.append((f"{GATEWAY_BASE}/middleware/idempotency/idempotency.ts", dedent(f"""\
        {GENERATED_BANNER}
        import {{ Request, Response, NextFunction }} from 'express';
        import Redis from 'ioredis';

        const redis = new Redis(process.env.PZO_REDIS_URL || 'redis://localhost:6379', {{
          maxRetriesPerRequest: 1, enableReadyCheck: false, lazyConnect: true,
        }});

        const IDEMPOTENCY_TTL_SECONDS = 86_400; // 24 hours
        const KEY_PREFIX = 'idempotency:';

        interface CachedResponse {{
          statusCode: number;
          body: string;
          headers: Record<string, string>;
        }}

        export function idempotencyGuard(req: Request, res: Response, next: NextFunction): void {{
          if (req.method === 'GET' || req.method === 'DELETE' || req.method === 'OPTIONS') {{
            next();
            return;
          }}

          const idempotencyKey = req.headers['idempotency-key'] as string | undefined;
          if (!idempotencyKey) {{
            next();
            return;
          }}

          const actorId = req.tenantContext?.actorId || 'anon';
          const redisKey = `${{KEY_PREFIX}}${{actorId}}:${{idempotencyKey}}`;

          redis.get(redisKey)
            .then((cached) => {{
              if (cached) {{
                const parsed: CachedResponse = JSON.parse(cached);
                for (const [k, v] of Object.entries(parsed.headers)) {{
                  res.setHeader(k, v);
                }}
                res.status(parsed.statusCode).end(parsed.body);
                return;
              }}

              const originalJson = res.json.bind(res);
              res.json = function (body: unknown): Response {{
                const cachedResp: CachedResponse = {{
                  statusCode: res.statusCode,
                  body: JSON.stringify(body),
                  headers: {{ 'content-type': 'application/json' }},
                }};

                redis.setex(redisKey, IDEMPOTENCY_TTL_SECONDS, JSON.stringify(cachedResp)).catch(() => {{}});

                return originalJson(body);
              }};

              next();
            }})
            .catch(() => {{
              next();
            }});
        }}
    """)))

