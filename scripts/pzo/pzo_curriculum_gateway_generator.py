def gen_migrations() -> List[Tuple[str, str]]:
    files = []

    for num, table_name, columns in MIGRATION_TABLES:
        col_defs = ",\n    ".join([f"{col} {typedef}" for col, typedef in columns])

        # Generate indexes for foreign keys and common query patterns
        indexes = []
        for col, typedef in columns:
            if "REFERENCES" in typedef and col != "id":
                indexes.append(f"CREATE INDEX IF NOT EXISTS idx_{table_name}_{col} ON {table_name}({col});")
            if col in ("slug", "status", "institution_id", "cohort_id", "created_at"):
                idx_name = f"idx_{table_name}_{col}"
                idx_line = f"CREATE INDEX IF NOT EXISTS {idx_name} ON {table_name}({col});"
