def run_generation(ctx: GeneratorContext) -> None:
    """Execute all generation passes in order."""

    generators = [
        ("Pass 1: Auth middleware", gen_auth_middleware),
        ("Pass 2: Observability middleware", gen_observability_middleware),
        ("Pass 3: Resilience middleware", gen_resilience_middleware),
        ("Pass 4: Security middleware", gen_security_middleware),
        ("Pass 5: Error handling", gen_error_middleware),
        ("Pass 6: Idempotency middleware", gen_idempotency_middleware),
        ("Pass 7: Feature flags", gen_feature_flags_middleware),
        ("Pass 8: Audit system", gen_audit_middleware),
        ("Pass 9: Validation middleware", gen_validation_middleware),
        ("Pass 10: Privacy middleware", gen_privacy_middleware),
        ("Pass 11: Contracts & DTOs", gen_contracts),
        ("Pass 12: Adapters", gen_adapters),
        ("Pass 13: Route modules", gen_routes),
        ("Pass 14: Health routes", gen_health_routes),
        ("Pass 15: Queue infrastructure", gen_queues),
        ("Pass 16: SQL Migrations", gen_migrations),
        ("Pass 17: OpenAPI spec", gen_openapi),
        ("Pass 18: Test suite", gen_tests),
        ("Pass 19: Documentation", gen_docs),
        ("Pass 20: Compatibility shim", gen_compatibility_shim),
    ]

    for pass_name, generator in generators:
        print(f"\n--- {pass_name} ---")
        files = generator()
        for rel_path, content in files:
            ctx.write_file(rel_path, content)

    # Final pass: checksum manifest
    print(f"\n--- Pass 21: Checksum manifest ---")
    for rel_path, content in gen_checksum_manifest(ctx):
        ctx.write_file(rel_path, content)


