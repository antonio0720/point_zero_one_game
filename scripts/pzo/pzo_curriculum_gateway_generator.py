class GeneratorContext:
    """Holds state for the generation run."""

    def __init__(self, repo_root: str, mode: str):
        self.repo_root = Path(repo_root).resolve()
        self.mode = mode  # plan | apply | verify
        self.files_generated: List[Tuple[str, str]] = []  # (path, sha256)
        self.files_backed_up: List[str] = []
        self.verification_errors: List[str] = []
        self.verification_warnings: List[str] = []
        self.stats = {"created": 0, "backed_up": 0, "skipped": 0}

    def resolve(self, relative_path: str) -> Path:
        return self.repo_root / relative_path

    def backup(self, relative_path: str) -> None:
        src = self.resolve(relative_path)
        if src.exists():
            backup_dir = self.repo_root / BACKUP_DIR_NAME / relative_path
            backup_dir.parent.mkdir(parents=True, exist_ok=True)
            shutil.copy2(str(src), str(backup_dir))
            self.files_backed_up.append(relative_path)
            self.stats["backed_up"] += 1

    def write_file(self, relative_path: str, content: str) -> None:
        full_path = self.resolve(relative_path)
        sha = hashlib.sha256(content.encode("utf-8")).hexdigest()

        if self.mode == "plan":
            print(f"  [PLAN] Would create: {relative_path} ({len(content)} bytes, sha256={sha[:12]}...)")
            self.files_generated.append((relative_path, sha))
            self.stats["created"] += 1
            return

        if self.mode == "apply":
            if full_path.exists():
                self.backup(relative_path)
            full_path.parent.mkdir(parents=True, exist_ok=True)
            full_path.write_text(content, encoding="utf-8")
            self.files_generated.append((relative_path, sha))
            self.stats["created"] += 1
            print(f"  [CREATED] {relative_path}")

    def verify_rule(self, rule_name: str, passed: bool, detail: str = "") -> None:
        status = "PASS" if passed else "FAIL"
        msg = f"  [{status}] {rule_name}"
        if detail:
            msg += f" — {detail}"
        print(msg)
        if not passed:
            self.verification_errors.append(f"{rule_name}: {detail}")

    def warn(self, msg: str) -> None:
        print(f"  [WARN] {msg}")
        self.verification_warnings.append(msg)


def dedent(text: str) -> str:
    """Remove common leading whitespace, strip leading/trailing blank lines."""
    return textwrap.dedent(text).strip() + "\n"


