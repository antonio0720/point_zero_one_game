
/**
 * ============================================================================
 * POINT ZERO ONE — SHARED CHAT LEARNING LABEL CONTRACTS
 * FILE: shared/contracts/chat/learning/LearningLabels.ts
 * ============================================================================
 *
 * Purpose
 * -------
 * Canonical label-schema, annotation, adjudication, truth-set, policy-outcome,
 * and dataset-target contracts for the unified chat intelligence lane.
 *
 * The event lane establishes atomic learning facts. The profile lane captures
 * durable player and room memory. The feature lane converts authoritative facts
 * into model-ready vectors. This file governs the next step: which outcomes are
 * considered training truth, how those truths are evidenced, how conflicting
 * labels are resolved, and how label bundles map back into helper timing,
 * hater targeting, moderation policy, negotiation intelligence, emotional
 * scoring, dramaturgy, and replay-linked evaluation.
 * ============================================================================
 */

