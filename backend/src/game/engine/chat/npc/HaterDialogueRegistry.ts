const clamp01 = (value: number | undefined): number => {
  if (typeof value !== 'number' || Number.isNaN(value)) return 0;
  if (value <= 0) return 0;
  if (value >= 1) return 1;
  return value;
};

const stableHash = (value: string): number => {
  let hash = 2166136261;
  for (let index = 0; index < value.length; index += 1) {
    hash ^= value.charCodeAt(index);
    hash = Math.imul(hash, 16777619);
  }
  return Math.abs(hash >>> 0);
};

const stableLineId = (personaId: HaterRegistryPersonaId, context: HaterDialogueContext, ordinal: number, text: string): string => {
  const suffix = stableHash(`${personaId}:${context}:${ordinal}:${text}`).toString(36);
  return `htr_${personaId.toLowerCase()}_${context.toLowerCase()}_${ordinal}_${suffix}`;
};

