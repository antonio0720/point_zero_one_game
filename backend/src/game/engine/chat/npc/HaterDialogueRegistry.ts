const materializeLines = (): Readonly<Record<HaterRegistryPersonaId, Readonly<Record<HaterDialogueContext, readonly HaterDialogueLine[]>>>> => {
  const personaEntries = Object.entries(RAW_HATER_DIALOGUE) as Array<[HaterRegistryPersonaId, typeof RAW_HATER_DIALOGUE[HaterRegistryPersonaId]]>;
  const personas: Partial<Record<HaterRegistryPersonaId, Readonly<Record<HaterDialogueContext, readonly HaterDialogueLine[]>>>> = {};
  for (const [personaId, byContext] of personaEntries) {
    const contextsOut: Partial<Record<HaterDialogueContext, readonly HaterDialogueLine[]>> = {};
    for (const context of DEFAULT_CONTEXTS) {
      const lines = (byContext[context] ?? []).map((line, index) => Object.freeze({
        id: stableLineId(personaId, context, index + 1, line.text),
        personaId,
        context,
        text: line.text,
        weight: line.weight,
        minTick: line.minTick,
        maxUses: line.maxUses,
        tags: line.tags,
        pressureBias: line.pressureBias,
        heatBias: line.heatBias,
        rivalryBias: line.rivalryBias,
        shadowEligible: line.shadowEligible,
        cadenceFloorMs: line.cadenceFloorMs,
      }) satisfies HaterDialogueLine);
      contextsOut[context] = Object.freeze(lines);
    }
    personas[personaId] = Object.freeze(contextsOut as Record<HaterDialogueContext, readonly HaterDialogueLine[]>);
  }
  return Object.freeze(personas as Record<HaterRegistryPersonaId, Readonly<Record<HaterDialogueContext, readonly HaterDialogueLine[]>>>);
};

const HATER_DIALOGUE_LINES = materializeLines();

const scoreLine = (line: HaterDialogueLine, input: HaterSelectionInput): number => {
  const tick = input.tick ?? 0;
  if (typeof line.minTick === 'number' && tick < line.minTick) return -Infinity;
  const uses = input.useCountByLineId?.[line.id] ?? 0;
  if (typeof line.maxUses === 'number' && uses >= line.maxUses) return -Infinity;
  if (input.shadowMode && !line.shadowEligible) return -Infinity;
  const banned = new Set(input.bannedTags ?? []);
  if (line.tags.some((tag) => banned.has(tag))) return -Infinity;
  const pressure = clamp01(input.pressure);
  const heat = clamp01(input.heat);
  const rivalry = clamp01(input.rivalry);
  const noveltyPenalty = uses * 0.14;
  const pressureFit = pressure * line.pressureBias;
  const heatFit = heat * line.heatBias;
  const rivalryFit = rivalry * line.rivalryBias;
  const recencyGuard = line.cadenceFloorMs >= 3000 ? 0.06 : 0;
  return line.weight + pressureFit + heatFit + rivalryFit - noveltyPenalty + recencyGuard;
};

export class HaterDialogueRegistry {
  private readonly profiles = HATER_PERSONA_PROFILES;
  private readonly lines = HATER_DIALOGUE_LINES;

  public listPersonaIds(): readonly HaterRegistryPersonaId[] {
    return Object.freeze(Object.keys(this.profiles) as HaterRegistryPersonaId[]);
  }

  public listProfiles(): readonly HaterPersonaProfile[] {
    return Object.freeze(this.listPersonaIds().map((id) => this.profiles[id]));
  }

  public hasPersona(personaId: string): personaId is HaterRegistryPersonaId {
    return Object.prototype.hasOwnProperty.call(this.profiles, personaId);
  }

  public getProfile(personaId: HaterRegistryPersonaId): HaterPersonaProfile {
    return this.profiles[personaId];
  }

  public byLegacyId(legacyId: string): HaterPersonaProfile | null {
    for (const personaId of this.listPersonaIds()) {
      const profile = this.profiles[personaId];
      if (profile.legacyId === legacyId) return profile;
    }
    return null;
  }

  public listContexts(): readonly HaterDialogueContext[] {
    return DEFAULT_CONTEXTS;
  }

  public getLines(personaId: HaterRegistryPersonaId, context: HaterDialogueContext): readonly HaterDialogueLine[] {
    return this.lines[personaId][context];
  }

  public getAllLinesForPersona(personaId: HaterRegistryPersonaId): readonly HaterDialogueLine[] {
    return Object.freeze(DEFAULT_CONTEXTS.flatMap((context) => this.lines[personaId][context]));
  }

  public selectBestLine(input: HaterSelectionInput): HaterDialogueLine | null {
    const personas = input.personaId ? [input.personaId] : this.listPersonaIds();
    let best: HaterDialogueLine | null = null;
    let bestScore = -Infinity;
    for (const personaId of personas) {
      for (const line of this.lines[personaId][input.context]) {
        const score = scoreLine(line, input);
        if (score > bestScore) {
          best = line;
          bestScore = score;
        }
      }
    }
    return best;
  }

  public rankScenario(input: HaterSelectionInput): readonly HaterScenarioCandidate[] {
    const personas = input.personaId ? [input.personaId] : this.listPersonaIds();
    const candidates: HaterScenarioCandidate[] = [];
    for (const personaId of personas) {
      for (const line of this.lines[personaId][input.context]) {
        const score = scoreLine(line, input);
        if (Number.isFinite(score)) {
          candidates.push({ persona: this.profiles[personaId], line, score });
        }
      }
    }
    candidates.sort((left, right) => right.score - left.score || left.line.id.localeCompare(right.line.id));
    return Object.freeze(candidates);
  }

  public resolvePersonaByMode(modeId: string | undefined): readonly HaterRegistryPersonaId[] {
    const normalized = (modeId ?? '').toLowerCase();
    if (normalized.includes('deal')) return Object.freeze(['LEGACY_HEIR', 'DEBT_DAEMON', 'BUREAUCRAT']);
    if (normalized.includes('lobby')) return Object.freeze(['BUREAUCRAT', 'WAGE_CAGE', 'LIQUIDATOR']);
    if (normalized.includes('syndicate')) return Object.freeze(['MANIPULATOR', 'STATUS_QUO_ML']);
    if (normalized.includes('battle') || normalized.includes('run')) return Object.freeze(['LIQUIDATOR', 'CRASH_PROPHET', 'MANIPULATOR', 'LEGACY_HEIR']);
    return this.listPersonaIds();
  }

  public resolveDefaultPersonaForContext(context: HaterDialogueContext): HaterRegistryPersonaId {
    switch (context) {
      case 'PLAYER_NEAR_BANKRUPTCY': return 'LIQUIDATOR';
      case 'PLAYER_SHIELD_BREAK': return 'CRASH_PROPHET';
      case 'PLAYER_IDLE': return 'MANIPULATOR';
      case 'PLAYER_INCOME_UP': return 'STATUS_QUO_ML';
      case 'NEAR_SOVEREIGNTY': return 'LEGACY_HEIR';
      default: return 'BUREAUCRAT';
    }
  }

  public getSnapshot(): HaterRegistrySnapshot {
    const linesByPersona = {} as Record<HaterRegistryPersonaId, number>;
    let totalLines = 0;
    for (const personaId of this.listPersonaIds()) {
      const count = this.getAllLinesForPersona(personaId).length;
      linesByPersona[personaId] = count;
      totalLines += count;
    }
    return Object.freeze({
      personas: this.listProfiles(),
      totalLines,
      contexts: this.listContexts(),
      linesByPersona: Object.freeze(linesByPersona),
    });
  }

  public buildAuditMatrix(): Readonly<Record<HaterRegistryPersonaId, Readonly<Record<HaterDialogueContext, number>>>> {
    const output: Partial<Record<HaterRegistryPersonaId, Readonly<Record<HaterDialogueContext, number>>>> = {};
    for (const personaId of this.listPersonaIds()) {
      const perContext: Partial<Record<HaterDialogueContext, number>> = {};
      for (const context of DEFAULT_CONTEXTS) {
        perContext[context] = this.lines[personaId][context].length;
      }
      output[personaId] = Object.freeze(perContext as Record<HaterDialogueContext, number>);
    }
    return Object.freeze(output as Record<HaterRegistryPersonaId, Readonly<Record<HaterDialogueContext, number>>>);
  }
}

