function computeEconomyImpact(
  effect: EffectPayload,
  snapshot: RunStateSnapshot,
): number {
  const cashPosition = snapshot.economy.cash;
  if (cashPosition <= 0) return 0;

  const cashDelta = effect.cashDelta ?? 0;
  const incomeDelta = (effect.incomeDelta ?? 0) * 12;
  const totalDelta = cashDelta + incomeDelta;
  return clamp01(totalDelta / Math.max(1, cashPosition) * 0.5 + 0.5);
}

function computeShieldImpact(
  effect: EffectPayload,
  snapshot: RunStateSnapshot,
): number {
  const shieldDelta = effect.shieldDelta ?? 0;
  if (shieldDelta === 0) return 0;

  const totalCurrent = snapshot.shield.layers.reduce((s, l) => s + l.current, 0);
  const totalMax = snapshot.shield.layers.reduce((s, l) => s + l.max, 0);
  if (totalMax === 0) return 0;

  const currentPct = totalCurrent / totalMax;
  const normalized = shieldDelta / totalMax;

  if (shieldDelta > 0) {
    return clamp01(normalized * (2 - currentPct));
  }

  return clamp01(-normalized * (1 + (1 - currentPct)));
}

function computePressureRelief(
  definition: CardDefinition,
  snapshot: RunStateSnapshot,
): number {
  const PRESSURE_ORDER = ['T0', 'T1', 'T2', 'T3', 'T4'];
  const tierIndex = PRESSURE_ORDER.indexOf(snapshot.pressure.tier);

  const hasPressureRelief =
    definition.tags.includes('resilience') ||
    definition.tags.includes('counter') ||
    definition.tags.includes('discipline') ||
    definition.deckType === 'COUNTER' ||
    definition.deckType === 'RESCUE' ||
    definition.deckType === 'DISCIPLINE';

  if (!hasPressureRelief) return 0;
  return clamp01((tierIndex / 4) * 1.2);
}

function computeTempoAdvantage(
  definition: CardDefinition,
  snapshot: RunStateSnapshot,
): number {
  if (snapshot.mode !== 'pvp') return 0;

  const tempoTags = ['tempo', 'sabotage', 'counter', 'bleed'];
  const hasTempo = definition.tags.some((t) => tempoTags.includes(t));
  if (!hasTempo) return 0;

  const battleBudgetRatio = snapshot.battle.battleBudget / Math.max(1, snapshot.battle.battleBudgetCap);
  return clamp01(0.3 + battleBudgetRatio * 0.7);
}

function computeTrustImpact(
  effect: EffectPayload,
  snapshot: RunStateSnapshot,
): number {
  if (snapshot.mode !== 'coop') return 0;
  const trustDelta = effect.trustDelta ?? 0;
  if (trustDelta === 0) return 0;
  return clamp01(Math.abs(trustDelta) / 20);
}

function computeDivergenceAlignment(
  definition: CardDefinition,
  snapshot: RunStateSnapshot,
): number {
  if (snapshot.mode !== 'ghost') return 0;

  const isGhostCard = definition.deckType === 'GHOST' || definition.tags.includes('divergence') || definition.tags.includes('precision');
  if (!isGhostCard) return 0;

  const gapProgress = clamp01(1 - Math.abs(snapshot.sovereignty.gapVsLegend));
  const markerProximity = isNearGhostMarker(snapshot) ? 0.4 : 0;
  return clamp01(gapProgress * 0.6 + markerProximity);
}

function computeCascadeTriggerProb(
  definition: CardDefinition,
  snapshot: RunStateSnapshot,
): number {
  const base = DECK_CASCADE_PROB[definition.deckType] ?? 0.1;
  const hasTag = definition.tags.includes('cascade') ? 1.3 : 1.0;
  const chainBonus = snapshot.cascade.activeChains.length > 0 ? 1.2 : 1.0;
  return clamp01(base * hasTag * chainBonus);
}

function computeUrgencyScore(
  definition: CardDefinition,
  snapshot: RunStateSnapshot,
  tagWeights: Record<string, number>,
): number {
  const PRESSURE_ORDER = ['T0', 'T1', 'T2', 'T3', 'T4'];
  const tierIndex = PRESSURE_ORDER.indexOf(snapshot.pressure.tier) / 4;
  const tierModifier = TIER_PLAY_VALUE_MODIFIER[snapshot.pressure.tier];

  const relevantTagScore = definition.tags.reduce((sum, tag) => {
    const weight = tagWeights[tag] ?? 1.0;
    return sum + weight;
  }, 0) / Math.max(1, definition.tags.length);

  const shieldPct = snapshot.shield.layers.reduce((s, l) => s + l.current, 0) /
    Math.max(1, snapshot.shield.layers.reduce((s, l) => s + l.max, 0));

  const timeRatio = snapshot.timers.elapsedMs /
    Math.max(1, snapshot.timers.seasonBudgetMs + snapshot.timers.extensionBudgetMs);

  const urgency = (relevantTagScore * 0.4 + tierIndex * 0.3 + (1 - shieldPct) * 0.2 + timeRatio * 0.1) * tierModifier;

  return clamp01(urgency);
}

function computeTagAffinities(
  definition: CardDefinition,
  snapshot: RunStateSnapshot,
): Record<string, number> {
  const affinities: Record<string, number> = {};
  const baseWeights = MODE_TAG_WEIGHTS[snapshot.mode];

  for (const tag of definition.tags) {
    const baseWeight = baseWeights[tag] ?? 1.0;
    const comboBonus = computeTagComboBonus(tag, definition.tags);
    affinities[tag] = roundThousandths(clamp01(baseWeight / 3.0 * (1 + comboBonus * 0.3)));
  }

  return affinities;
}

function computeTagComboBonus(tag: string, allTags: readonly string[]): number {
  const comboRow = TAG_COMBO_MATRIX[tag];
  if (!comboRow) return 0;

  let bonus = 0;
  for (const other of allTags) {
    if (other !== tag && comboRow[other] !== undefined) {
      bonus += comboRow[other];
    }
  }

  return Math.min(1.0, bonus);
}

function buildMLScore(
  definition: CardDefinition,
  resolved: Omit<ResolvedCardView, 'mlScore' | 'dlInputVector' | 'playRecommendation'>,
  snapshot: RunStateSnapshot,
): CardMLScore {
  const economyImpact = computeEconomyImpact(resolved.effect, snapshot);
  const shieldImpact = computeShieldImpact(resolved.effect, snapshot);
  const pressureRelief = computePressureRelief(definition, snapshot);
  const tempoAdvantage = computeTempoAdvantage(definition, snapshot);
  const trustImpact = computeTrustImpact(resolved.effect, snapshot);
  const divergenceAlignment = computeDivergenceAlignment(definition, snapshot);
  const cascadeTriggerProb = computeCascadeTriggerProb(definition, snapshot);
  const urgencyScore = computeUrgencyScore(definition, snapshot, resolved.tagWeights);
  const tagAffinities = computeTagAffinities(definition, snapshot);

  const playValue = clamp01(
    economyImpact * 0.25 +
    shieldImpact * 0.15 +
    pressureRelief * 0.15 +
    tempoAdvantage * 0.1 +
    trustImpact * 0.1 +
    divergenceAlignment * 0.1 +
    cascadeTriggerProb * 0.1 +
    urgencyScore * 0.05,
  );

  const confidence = clamp01(
    0.5 + snapshot.telemetry.decisions.filter((d) => d.accepted).length / 40,
  );

  return {
    playValue: roundThousandths(playValue),
    economyImpact: roundThousandths(economyImpact),
    shieldImpact: roundThousandths(shieldImpact),
    pressureRelief: roundThousandths(pressureRelief),
    cascadeTriggerProb: roundThousandths(cascadeTriggerProb),
    tempoAdvantage: roundThousandths(tempoAdvantage),
    trustImpact: roundThousandths(trustImpact),
    divergenceAlignment: roundThousandths(divergenceAlignment),
    urgencyScore: roundThousandths(urgencyScore),
    confidence: roundThousandths(confidence),
    tagAffinities,
    modelVersion: ML_MODEL_VERSION,
  };
}

function buildDLInputVector(
  definition: CardDefinition,
  resolved: Omit<ResolvedCardView, 'mlScore' | 'dlInputVector' | 'playRecommendation'>,
  snapshot: RunStateSnapshot,
): number[] {
  const PRESSURE_ORDER = ['T0', 'T1', 'T2', 'T3', 'T4'];
  const PHASE_ORDER = ['FOUNDATION', 'ESCALATION', 'SOVEREIGNTY'];
  const MODE_ORDER = ['solo', 'pvp', 'coop', 'ghost'];
  const DECK_ORDER = DECK_TYPES as unknown as DeckType[];
  const TIMING_ORDER = TIMING_CLASSES as unknown as TimingClass[];

  const deckIndex = DECK_ORDER.indexOf(definition.deckType);
  const primaryTiming = resolved.timingClass[0] ?? 'ANY';
  const timingIndex = TIMING_ORDER.indexOf(primaryTiming);

  const shieldTotal = snapshot.shield.layers.reduce((s, l) => s + l.current, 0);
  const shieldMax = snapshot.shield.layers.reduce((s, l) => s + l.max, 0);

  return [
    // Card features
    resolved.cost / 100_000,
    deckIndex / Math.max(1, DECK_ORDER.length),
    timingIndex / Math.max(1, TIMING_ORDER.length),
    definition.autoResolve ? 1 : 0,
    definition.tags.length / 10,
    ['LEGENDARY', 'RARE', 'UNCOMMON', 'COMMON'].indexOf(definition.rarity) / 3,
    resolved.divergencePotential === 'HIGH' ? 1 : resolved.divergencePotential === 'MEDIUM' ? 0.5 : 0,

    // Snapshot context
    PRESSURE_ORDER.indexOf(snapshot.pressure.tier) / 4,
    snapshot.pressure.score,
    PHASE_ORDER.indexOf(snapshot.phase) / 2,
    MODE_ORDER.indexOf(snapshot.mode) / 3,
    shieldMax === 0 ? 1 : shieldTotal / shieldMax,
    snapshot.economy.cash / 500_000,
    snapshot.economy.netWorth / 1_000_000,
    snapshot.economy.incomePerTick / 50_000,
    snapshot.tension.score,
    snapshot.tension.anticipation,
    snapshot.cascade.activeChains.length / 10,
    snapshot.cards.hand.length / 8,
    snapshot.modeState.bleedMode ? 1 : 0,
    snapshot.battle.firstBloodClaimed ? 1 : 0,
    snapshot.sovereignty.gapVsLegend,
    snapshot.timers.elapsedMs / Math.max(1, snapshot.timers.seasonBudgetMs),

    // Resource state
    resolved.resourceType === 'cash' ? 1 : 0,
    resolved.resourceType === 'battle_budget' ? 1 : 0,
    resolved.resourceType === 'shared_treasury' ? 1 : 0,
    resolved.resourceType === 'free' ? 1 : 0,
  ];
}

function buildPlayRecommendation(
  definition: CardDefinition,
  resolved: Omit<ResolvedCardView, 'mlScore' | 'dlInputVector' | 'playRecommendation'>,
  mlScore: CardMLScore,
  snapshot: RunStateSnapshot,
): PlayRecommendation {
  const rationale: string[] = [];
  const alternativeTags: string[] = [];

  if (!resolved.legal) {
    return {
      recommended: false,
      priority: 'AVOID',
      rationale: [...resolved.legalityReasons],
      alternativeTags: [],
      bestTimingClass: null,
      estimatedEffectiveness: 0,
    };
  }

  let priority: PlayRecommendation['priority'] = 'MEDIUM';

  if (mlScore.urgencyScore >= 0.8) {
    priority = 'MUST_PLAY';
    rationale.push(`Urgency score ${mlScore.urgencyScore.toFixed(2)} — critical play window.`);
  } else if (mlScore.playValue >= 0.7) {
    priority = 'HIGH';
    rationale.push(`Play value ${mlScore.playValue.toFixed(2)} — high expected return.`);
  } else if (mlScore.playValue >= 0.45) {
    priority = 'MEDIUM';
  } else if (mlScore.playValue >= 0.2) {
    priority = 'LOW';
    rationale.push(`Play value ${mlScore.playValue.toFixed(2)} — marginal return.`);
  } else {
    priority = 'AVOID';
    rationale.push(`Play value ${mlScore.playValue.toFixed(2)} — low priority in current context.`);
  }

  if (mlScore.economyImpact >= 0.6) {
    rationale.push(`Strong economy impact: ${mlScore.economyImpact.toFixed(2)}.`);
  }

  if (mlScore.pressureRelief >= 0.5) {
    rationale.push(`Meaningful pressure relief at ${snapshot.pressure.tier}.`);
  }

  if (mlScore.cascadeTriggerProb >= 0.35) {
    rationale.push(`${(mlScore.cascadeTriggerProb * 100).toFixed(0)}% cascade trigger probability.`);
  }

  if (snapshot.mode === 'pvp' && mlScore.tempoAdvantage >= 0.5) {
    rationale.push(`High tempo advantage: ${mlScore.tempoAdvantage.toFixed(2)}.`);
  }

  if (snapshot.mode === 'ghost' && mlScore.divergenceAlignment >= 0.5) {
    rationale.push(`Aligned with legend gap at ${snapshot.sovereignty.gapVsLegend.toFixed(3)}.`);
  }

  const baseWeights = MODE_TAG_WEIGHTS[snapshot.mode];
  const sortedTags = Object.entries(baseWeights)
    .filter(([tag]) => !definition.tags.includes(tag))
    .sort(([, a], [, b]) => b - a)
    .slice(0, 3)
    .map(([tag]) => tag);
  alternativeTags.push(...sortedTags);

  const bestTimingClass = resolved.timingClass[0] ?? null;

  return {
    recommended: priority !== 'AVOID',
    priority,
    rationale: Object.freeze(rationale),
    alternativeTags: Object.freeze(alternativeTags),
    bestTimingClass,
    estimatedEffectiveness: roundThousandths(mlScore.playValue * (1 + mlScore.urgencyScore * 0.5)),
  };
}

