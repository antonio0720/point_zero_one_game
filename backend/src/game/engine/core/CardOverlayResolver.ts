  /**
   * Resolve a batch of card definitions in one pass.
   * Optimized for deck composition scanning.
   */
  public resolveBatch(
    definitions: readonly CardDefinition[],
    snapshot: RunStateSnapshot,
  ): ResolvedCardView[] {
    return definitions.map((def) => this.resolveDefinition(def, snapshot));
  }

  /**
   * Resolve all hand instances and sort by ML urgency descending.
   */
  public resolveHandByPriority(
    instances: readonly CardInstance[],
    snapshot: RunStateSnapshot,
  ): ResolvedCardView[] {
    return instances
      .map((instance) => this.resolveInstanceForPlay(instance, snapshot))
      .sort((a, b) => b.mlScore.urgencyScore - a.mlScore.urgencyScore);
  }

  /**
   * Filter hand to only playable cards.
   */
  public filterPlayable(
    instances: readonly CardInstance[],
    snapshot: RunStateSnapshot,
    availableTimingClasses: readonly TimingClass[] = ['ANY'],
  ): Array<{ instance: CardInstance; resolved: ResolvedCardView }> {
    return instances
      .map((instance) => ({
        instance,
        resolved: this.resolveInstanceForPlay(instance, snapshot),
      }))
      .filter(({ resolved }) => {
        if (!resolved.legal) return false;
        return resolved.timingClass.some((t) => availableTimingClasses.includes(t) || t === 'ANY');
      });
  }

