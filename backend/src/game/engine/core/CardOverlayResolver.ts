// ---------------------------------------------------------------------------
// CardOverlayRollingStats — tracks validation throughput over 60 ticks
// ---------------------------------------------------------------------------

export interface CardOverlayTickStats {
  readonly tick: number;
  readonly validations: number;
  readonly playableCount: number;
  readonly blockedCount: number;
  readonly avgValidationMs: number;
}

export class CardOverlayRollingStats {
  private readonly capacity: number;
  private readonly snapshots: CardOverlayTickStats[] = [];

  public constructor(capacity = 60) { this.capacity = capacity; }

  public record(snap: CardOverlayTickStats): void {
    if (this.snapshots.length >= this.capacity) this.snapshots.shift();
    this.snapshots.push(Object.freeze(snap));
  }

  public avgPlayableRate(): number {
    if (this.snapshots.length === 0) return 1;
    const total = this.snapshots.reduce((s, r) => s + r.validations, 0);
    const playable = this.snapshots.reduce((s, r) => s + r.playableCount, 0);
    return total > 0 ? playable / total : 1;
  }

  public avgValidationMs(): number {
    if (this.snapshots.length === 0) return 0;
    return this.snapshots.reduce((s, r) => s + r.avgValidationMs, 0) / this.snapshots.length;
  }

  public totalValidations(): number {
    return this.snapshots.reduce((s, r) => s + r.validations, 0);
  }

  public peakValidationsPerTick(): number {
    if (this.snapshots.length === 0) return 0;
    return Math.max(...this.snapshots.map(s => s.validations));
  }

  public clear(): void { this.snapshots.length = 0; }
  public size(): number { return this.snapshots.length; }
}

// ---------------------------------------------------------------------------
// CardOverlayHealthGrader
// ---------------------------------------------------------------------------

export type CardOverlayHealthGrade = 'S' | 'A' | 'B' | 'C' | 'D' | 'F';

export function gradeCardOverlayHealth(
  avgPlayableRate: number,
  avgValidationMs: number,
): CardOverlayHealthGrade {
  const timeScore = Math.max(0, 1 - avgValidationMs / 50);
  const composite = avgPlayableRate * 0.7 + timeScore * 0.3;
  if (composite >= 0.93) return 'S';
  if (composite >= 0.82) return 'A';
  if (composite >= 0.68) return 'B';
  if (composite >= 0.50) return 'C';
  if (composite >= 0.32) return 'D';
  return 'F';
}

export interface CardOverlayHealthSummary {
  readonly grade: CardOverlayHealthGrade;
  readonly avgPlayableRate: number;
  readonly avgValidationMs: number;
  readonly totalValidations: number;
  readonly peakValidationsPerTick: number;
  readonly isHealthy: boolean;
}

export function buildCardOverlayHealthSummary(
  stats: CardOverlayRollingStats,
): CardOverlayHealthSummary {
  const avgPlayableRate = stats.avgPlayableRate();
  const avgValidationMs = stats.avgValidationMs();
  const grade = gradeCardOverlayHealth(avgPlayableRate, avgValidationMs);
  return Object.freeze({
    grade, avgPlayableRate, avgValidationMs,
    totalValidations: stats.totalValidations(),
    peakValidationsPerTick: stats.peakValidationsPerTick(),
    isHealthy: grade === 'S' || grade === 'A' || grade === 'B',
  });
}

