BEGIN;

CREATE OR REPLACE FUNCTION set_row_updated_at()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
  NEW.updated_at = CURRENT_TIMESTAMP;
  RETURN NEW;
END;
$$;

-- ============================================================================
-- BILLING DOMAIN
-- Anchors billing to accounts(id), because accounts is the stable table created
-- in the real Postgres baseline migration.
-- Uses *_cents BIGINT instead of DECIMAL to match repo money conventions.
-- ============================================================================

CREATE TABLE IF NOT EXISTS billing_plans (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  code VARCHAR(64) NOT NULL,
  name VARCHAR(255) NOT NULL,
  description TEXT,
  price_cents BIGINT NOT NULL DEFAULT 0,
  currency CHAR(3) NOT NULL DEFAULT 'USD',
  billing_interval VARCHAR(16) NOT NULL DEFAULT 'monthly',
  is_active BOOLEAN NOT NULL DEFAULT TRUE,
  metadata JSONB NOT NULL DEFAULT '{}'::jsonb,
  created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT billing_plans_code_not_blank
    CHECK (char_length(btrim(code)) > 0),
  CONSTRAINT billing_plans_name_not_blank
    CHECK (char_length(btrim(name)) > 0),
  CONSTRAINT billing_plans_price_cents_nonnegative
    CHECK (price_cents >= 0),
  CONSTRAINT billing_plans_currency_allowed
    CHECK (currency IN ('USD', 'EUR', 'GBP')),
  CONSTRAINT billing_plans_interval_allowed
    CHECK (billing_interval IN ('monthly', 'quarterly', 'yearly', 'one_time')),
  CONSTRAINT billing_plans_metadata_is_object
    CHECK (jsonb_typeof(metadata) = 'object')
);

CREATE UNIQUE INDEX IF NOT EXISTS billing_plans_code_uidx
  ON billing_plans ((lower(btrim(code))));
CREATE INDEX IF NOT EXISTS billing_plans_is_active_idx
  ON billing_plans (is_active);
CREATE INDEX IF NOT EXISTS billing_plans_currency_interval_idx
  ON billing_plans (currency, billing_interval);

DROP TRIGGER IF EXISTS billing_plans_set_updated_at ON billing_plans;
CREATE TRIGGER billing_plans_set_updated_at
BEFORE UPDATE ON billing_plans
FOR EACH ROW
EXECUTE FUNCTION set_row_updated_at();

CREATE TABLE IF NOT EXISTS invoices (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  account_id INTEGER NOT NULL REFERENCES accounts(id) ON DELETE RESTRICT,
  billing_plan_id INTEGER REFERENCES billing_plans(id) ON DELETE SET NULL,
  invoice_number VARCHAR(64) NOT NULL,
  status VARCHAR(16) NOT NULL DEFAULT 'draft',
  currency CHAR(3) NOT NULL DEFAULT 'USD',
  period_start TIMESTAMPTZ,
  period_end TIMESTAMPTZ,
  issued_at TIMESTAMPTZ,
  due_at TIMESTAMPTZ,
  paid_at TIMESTAMPTZ,
  subtotal_cents BIGINT NOT NULL DEFAULT 0,
  tax_cents BIGINT NOT NULL DEFAULT 0,
  discount_cents BIGINT NOT NULL DEFAULT 0,
  external_reference VARCHAR(255),
  metadata JSONB NOT NULL DEFAULT '{}'::jsonb,
  created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT invoices_invoice_number_not_blank
    CHECK (char_length(btrim(invoice_number)) > 0),
  CONSTRAINT invoices_status_allowed
    CHECK (status IN ('draft', 'issued', 'paid', 'void', 'overdue')),
  CONSTRAINT invoices_currency_allowed
    CHECK (currency IN ('USD', 'EUR', 'GBP')),
  CONSTRAINT invoices_subtotal_cents_nonnegative
    CHECK (subtotal_cents >= 0),
  CONSTRAINT invoices_tax_cents_nonnegative
    CHECK (tax_cents >= 0),
  CONSTRAINT invoices_discount_cents_nonnegative
    CHECK (discount_cents >= 0),
  CONSTRAINT invoices_period_valid
    CHECK (
      period_start IS NULL
      OR period_end IS NULL
      OR period_end >= period_start
    ),
  CONSTRAINT invoices_due_after_issue
    CHECK (
      issued_at IS NULL
      OR due_at IS NULL
      OR due_at >= issued_at
    ),
  CONSTRAINT invoices_paid_after_issue
    CHECK (
      issued_at IS NULL
      OR paid_at IS NULL
      OR paid_at >= issued_at
    ),
  CONSTRAINT invoices_metadata_is_object
    CHECK (jsonb_typeof(metadata) = 'object')
);

CREATE UNIQUE INDEX IF NOT EXISTS invoices_invoice_number_uidx
  ON invoices ((lower(btrim(invoice_number))));
CREATE INDEX IF NOT EXISTS invoices_account_id_idx
  ON invoices (account_id);
CREATE INDEX IF NOT EXISTS invoices_billing_plan_id_idx
  ON invoices (billing_plan_id)
  WHERE billing_plan_id IS NOT NULL;
CREATE INDEX IF NOT EXISTS invoices_status_due_at_idx
  ON invoices (status, due_at);
CREATE INDEX IF NOT EXISTS invoices_created_at_idx
  ON invoices (created_at DESC);

DROP TRIGGER IF EXISTS invoices_set_updated_at ON invoices;
CREATE TRIGGER invoices_set_updated_at
BEFORE UPDATE ON invoices
FOR EACH ROW
EXECUTE FUNCTION set_row_updated_at();

