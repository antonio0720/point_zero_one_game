CREATE TABLE IF NOT EXISTS entitlements (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    account_id INTEGER NOT NULL REFERENCES accounts(id) ON DELETE CASCADE,
    entitlement_key VARCHAR(128) NOT NULL,
    entitlement_type VARCHAR(64) NOT NULL,
    season_id VARCHAR(64),
    source_ref VARCHAR(255),
    state entitlement_state NOT NULL DEFAULT 'ACTIVE',
    metadata_json JSONB NOT NULL DEFAULT '{}'::jsonb,
    effective_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    expires_at TIMESTAMPTZ,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT entitlements_key_not_blank
        CHECK (char_length(btrim(entitlement_key)) > 0),
    CONSTRAINT entitlements_type_not_blank
        CHECK (char_length(btrim(entitlement_type)) > 0),
    CONSTRAINT entitlements_metadata_object
        CHECK (jsonb_typeof(metadata_json) = 'object'),
    CONSTRAINT entitlements_expiry_after_effective
        CHECK (
            expires_at IS NULL
            OR expires_at >= effective_at
        ),
    CONSTRAINT entitlements_account_key_unique
        UNIQUE (account_id, entitlement_key)
);

