BEGIN;

CREATE TABLE IF NOT EXISTS cosmetic_types (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT cosmetic_types_name_not_blank CHECK (char_length(btrim(name)) > 0)
);

CREATE TABLE IF NOT EXISTS cosmetics (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    type_id INTEGER NOT NULL REFERENCES cosmetic_types(id) ON DELETE CASCADE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT cosmetics_name_not_blank CHECK (char_length(btrim(name)) > 0)
);

CREATE TABLE IF NOT EXISTS cosmetic_unlocks (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    cosmetic_id INTEGER NOT NULL REFERENCES cosmetics(id) ON DELETE CASCADE,
    source VARCHAR(255) NOT NULL,
    earned_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT cosmetic_unlocks_source_not_blank CHECK (char_length(btrim(source)) > 0)
);

CREATE UNIQUE INDEX IF NOT EXISTS cosmetic_types_name_uidx
    ON cosmetic_types (name);

CREATE UNIQUE INDEX IF NOT EXISTS cosmetics_name_type_id_uidx
    ON cosmetics (name, type_id);

CREATE INDEX IF NOT EXISTS cosmetics_type_id_idx
    ON cosmetics (type_id);

CREATE UNIQUE INDEX IF NOT EXISTS cosmetic_unlocks_user_id_cosmetic_id_uidx
    ON cosmetic_unlocks (user_id, cosmetic_id);

CREATE INDEX IF NOT EXISTS cosmetic_unlocks_cosmetic_id_idx
    ON cosmetic_unlocks (cosmetic_id);

CREATE INDEX IF NOT EXISTS cosmetic_unlocks_source_idx
    ON cosmetic_unlocks (source);

CREATE INDEX IF NOT EXISTS cosmetic_unlocks_earned_at_idx
    ON cosmetic_unlocks (earned_at);

DROP TRIGGER IF EXISTS cosmetic_types_set_updated_at ON cosmetic_types;
CREATE TRIGGER cosmetic_types_set_updated_at
BEFORE UPDATE ON cosmetic_types
FOR EACH ROW
EXECUTE FUNCTION set_row_updated_at();

DROP TRIGGER IF EXISTS cosmetics_set_updated_at ON cosmetics;
CREATE TRIGGER cosmetics_set_updated_at
BEFORE UPDATE ON cosmetics
FOR EACH ROW
EXECUTE FUNCTION set_row_updated_at();

DROP TRIGGER IF EXISTS cosmetic_unlocks_set_updated_at ON cosmetic_unlocks;
CREATE TRIGGER cosmetic_unlocks_set_updated_at
BEFORE UPDATE ON cosmetic_unlocks
FOR EACH ROW
EXECUTE FUNCTION set_row_updated_at();

COMMIT;