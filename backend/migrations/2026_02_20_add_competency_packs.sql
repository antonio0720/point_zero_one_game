-- Postgres-native rewrite aligned to backend migration style and pack-authoring contracts.
-- Assumes 0001_init_schema.sql has already created set_row_updated_at().

BEGIN;

CREATE TABLE IF NOT EXISTS competency_packs (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    slug VARCHAR(128) NOT NULL,
    title VARCHAR(255) NOT NULL,
    description TEXT,
    state VARCHAR(32) NOT NULL DEFAULT 'draft',
    current_version_number INTEGER NOT NULL DEFAULT 1,
    published BOOLEAN NOT NULL DEFAULT FALSE,
    published_at TIMESTAMPTZ,
    content_hash VARCHAR(128),
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT competency_packs_slug_not_blank
        CHECK (char_length(btrim(slug)) > 0),
    CONSTRAINT competency_packs_title_not_blank
        CHECK (char_length(btrim(title)) > 0),
    CONSTRAINT competency_packs_state_valid
        CHECK (state IN ('draft', 'ready', 'validating', 'published')),
    CONSTRAINT competency_packs_current_version_positive
        CHECK (current_version_number > 0)
);

CREATE UNIQUE INDEX IF NOT EXISTS competency_packs_slug_uidx
    ON competency_packs ((lower(btrim(slug))));

CREATE INDEX IF NOT EXISTS competency_packs_state_idx
    ON competency_packs (state);

CREATE INDEX IF NOT EXISTS competency_packs_published_idx
    ON competency_packs (published);

DROP TRIGGER IF EXISTS competency_packs_set_updated_at ON competency_packs;
CREATE TRIGGER competency_packs_set_updated_at
BEFORE UPDATE ON competency_packs
FOR EACH ROW
EXECUTE FUNCTION set_row_updated_at();

CREATE TABLE IF NOT EXISTS competency_pack_versions (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    pack_id INTEGER NOT NULL REFERENCES competency_packs(id) ON DELETE CASCADE,
    version_number INTEGER NOT NULL,
    version_label VARCHAR(32),
    content_hash VARCHAR(128),
    version_pin_set JSONB NOT NULL DEFAULT '[]'::jsonb,
    publish_actor VARCHAR(255),
    published_at TIMESTAMPTZ,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT competency_pack_versions_version_number_positive
        CHECK (version_number > 0),
    CONSTRAINT competency_pack_versions_version_pin_set_is_array
        CHECK (jsonb_typeof(version_pin_set) = 'array'),
    CONSTRAINT competency_pack_versions_pack_version_unique
        UNIQUE (pack_id, version_number)
);

CREATE INDEX IF NOT EXISTS competency_pack_versions_pack_id_idx
    ON competency_pack_versions (pack_id);

CREATE INDEX IF NOT EXISTS competency_pack_versions_published_at_idx
    ON competency_pack_versions (published_at DESC);

DROP TRIGGER IF EXISTS competency_pack_versions_set_updated_at ON competency_pack_versions;
CREATE TRIGGER competency_pack_versions_set_updated_at
BEFORE UPDATE ON competency_pack_versions
FOR EACH ROW
EXECUTE FUNCTION set_row_updated_at();

CREATE TABLE IF NOT EXISTS competency_pack_version_scenarios (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    version_id INTEGER NOT NULL REFERENCES competency_pack_versions(id) ON DELETE CASCADE,
    scenario_key VARCHAR(128) NOT NULL,
    name VARCHAR(255) NOT NULL,
    description TEXT,
    sort_order INTEGER NOT NULL DEFAULT 0,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT competency_pack_version_scenarios_key_not_blank
        CHECK (char_length(btrim(scenario_key)) > 0),
    CONSTRAINT competency_pack_version_scenarios_name_not_blank
        CHECK (char_length(btrim(name)) > 0),
    CONSTRAINT competency_pack_version_scenarios_sort_order_nonnegative
        CHECK (sort_order >= 0),
    CONSTRAINT competency_pack_version_scenarios_unique
        UNIQUE (version_id, scenario_key)
);

CREATE INDEX IF NOT EXISTS competency_pack_version_scenarios_version_id_idx
    ON competency_pack_version_scenarios (version_id);

CREATE INDEX IF NOT EXISTS competency_pack_version_scenarios_sort_order_idx
    ON competency_pack_version_scenarios (version_id, sort_order);

DROP TRIGGER IF EXISTS competency_pack_version_scenarios_set_updated_at
    ON competency_pack_version_scenarios;
CREATE TRIGGER competency_pack_version_scenarios_set_updated_at
BEFORE UPDATE ON competency_pack_version_scenarios
FOR EACH ROW
EXECUTE FUNCTION set_row_updated_at();

CREATE TABLE IF NOT EXISTS competency_pack_rubrics (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    version_scenario_id INTEGER NOT NULL REFERENCES competency_pack_version_scenarios(id) ON DELETE CASCADE,
    name VARCHAR(255) NOT NULL,
    description TEXT,
    weights JSONB NOT NULL DEFAULT '{}'::jsonb,
    sort_order INTEGER NOT NULL DEFAULT 0,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT competency_pack_rubrics_name_not_blank
        CHECK (char_length(btrim(name)) > 0),
    CONSTRAINT competency_pack_rubrics_weights_is_object
        CHECK (jsonb_typeof(weights) = 'object'),
    CONSTRAINT competency_pack_rubrics_sort_order_nonnegative
        CHECK (sort_order >= 0),
    CONSTRAINT competency_pack_rubrics_unique
        UNIQUE (version_scenario_id, name)
);

CREATE INDEX IF NOT EXISTS competency_pack_rubrics_version_scenario_id_idx
    ON competency_pack_rubrics (version_scenario_id);

CREATE INDEX IF NOT EXISTS competency_pack_rubrics_sort_order_idx
    ON competency_pack_rubrics (version_scenario_id, sort_order);

DROP TRIGGER IF EXISTS competency_pack_rubrics_set_updated_at ON competency_pack_rubrics;
CREATE TRIGGER competency_pack_rubrics_set_updated_at
BEFORE UPDATE ON competency_pack_rubrics
FOR EACH ROW
EXECUTE FUNCTION set_row_updated_at();

CREATE TABLE IF NOT EXISTS competency_pack_debrief_cards (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    rubric_id INTEGER NOT NULL REFERENCES competency_pack_rubrics(id) ON DELETE CASCADE,
    title VARCHAR(255) NOT NULL,
    body TEXT NOT NULL,
    sort_order INTEGER NOT NULL DEFAULT 0,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT competency_pack_debrief_cards_title_not_blank
        CHECK (char_length(btrim(title)) > 0),
    CONSTRAINT competency_pack_debrief_cards_body_not_blank
        CHECK (char_length(btrim(body)) > 0),
    CONSTRAINT competency_pack_debrief_cards_sort_order_nonnegative
        CHECK (sort_order >= 0),
    CONSTRAINT competency_pack_debrief_cards_rubric_sort_unique
        UNIQUE (rubric_id, sort_order)
);

CREATE INDEX IF NOT EXISTS competency_pack_debrief_cards_rubric_id_idx
    ON competency_pack_debrief_cards (rubric_id);

DROP TRIGGER IF EXISTS competency_pack_debrief_cards_set_updated_at ON competency_pack_debrief_cards;
CREATE TRIGGER competency_pack_debrief_cards_set_updated_at
BEFORE UPDATE ON competency_pack_debrief_cards
FOR EACH ROW
EXECUTE FUNCTION set_row_updated_at();

COMMIT;