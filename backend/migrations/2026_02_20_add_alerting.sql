    'info',
    'warning',
    'critical'
  );
EXCEPTION
  WHEN duplicate_object THEN NULL;
END
$$;

DO $$
BEGIN
  CREATE TYPE alert_firing_state AS ENUM (
    'open',
    'acknowledged',
    'suppressed',
    'resolved'
  );
EXCEPTION
  WHEN duplicate_object THEN NULL;
END
$$;

CREATE TABLE IF NOT EXISTS alert_rules (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(255) NOT NULL,
  description TEXT,
  aggregate_kind VARCHAR(64),
  event_name VARCHAR(128),
  severity alert_severity NOT NULL DEFAULT 'warning',
  is_enabled BOOLEAN NOT NULL DEFAULT TRUE,
  dedupe_window_seconds INTEGER NOT NULL DEFAULT 300,
  rule_json JSONB NOT NULL DEFAULT '{}'::jsonb,
  created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT alert_rules_name_not_blank
    CHECK (char_length(btrim(name)) > 0),
  CONSTRAINT alert_rules_aggregate_kind_not_blank
    CHECK (aggregate_kind IS NULL OR char_length(btrim(aggregate_kind)) > 0),
  CONSTRAINT alert_rules_event_name_not_blank
    CHECK (event_name IS NULL OR char_length(btrim(event_name)) > 0),
  CONSTRAINT alert_rules_dedupe_window_nonnegative
    CHECK (dedupe_window_seconds >= 0),
  CONSTRAINT alert_rules_rule_json_object
    CHECK (jsonb_typeof(rule_json) = 'object')
);

CREATE TABLE IF NOT EXISTS alert_firings (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  rule_id INTEGER NOT NULL REFERENCES alert_rules(id) ON DELETE CASCADE,
  run_id INTEGER REFERENCES runs(id) ON DELETE SET NULL,
  run_event_id INTEGER REFERENCES run_events(id) ON DELETE SET NULL,
  global_event_store_id BIGINT REFERENCES global_event_store(id) ON DELETE SET NULL,
  aggregate_kind VARCHAR(64),
  aggregate_id VARCHAR(255),
  event_name VARCHAR(128),
  fingerprint VARCHAR(255) NOT NULL,
  state alert_firing_state NOT NULL DEFAULT 'open',
  context_json JSONB NOT NULL DEFAULT '{}'::jsonb,
  opened_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  acknowledged_at TIMESTAMPTZ,
  suppressed_at TIMESTAMPTZ,
  resolved_at TIMESTAMPTZ,
  created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT alert_firings_fingerprint_not_blank
    CHECK (char_length(btrim(fingerprint)) > 0),
  CONSTRAINT alert_firings_aggregate_kind_not_blank
    CHECK (aggregate_kind IS NULL OR char_length(btrim(aggregate_kind)) > 0),
  CONSTRAINT alert_firings_aggregate_id_not_blank
    CHECK (aggregate_id IS NULL OR char_length(btrim(aggregate_id)) > 0),
  CONSTRAINT alert_firings_event_name_not_blank
    CHECK (event_name IS NULL OR char_length(btrim(event_name)) > 0),
  CONSTRAINT alert_firings_context_json_object
    CHECK (jsonb_typeof(context_json) = 'object'),
  CONSTRAINT alert_firings_ack_after_open
    CHECK (acknowledged_at IS NULL OR acknowledged_at >= opened_at),
  CONSTRAINT alert_firings_suppress_after_open
    CHECK (suppressed_at IS NULL OR suppressed_at >= opened_at),
  CONSTRAINT alert_firings_resolve_after_open
    CHECK (resolved_at IS NULL OR resolved_at >= opened_at)
);

CREATE TABLE IF NOT EXISTS alert_suppressions (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  firing_id INTEGER NOT NULL REFERENCES alert_firings(id) ON DELETE CASCADE,
  suppressed_by_user_id INTEGER REFERENCES users(id) ON DELETE SET NULL,
  suppression_reason TEXT NOT NULL,
  scope_json JSONB NOT NULL DEFAULT '{}'::jsonb,
  created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  expires_at TIMESTAMPTZ,
  CONSTRAINT alert_suppressions_reason_not_blank
    CHECK (char_length(btrim(suppression_reason)) > 0),
  CONSTRAINT alert_suppressions_scope_json_object
    CHECK (jsonb_typeof(scope_json) = 'object'),
  CONSTRAINT alert_suppressions_expiry_after_create
    CHECK (expires_at IS NULL OR expires_at >= created_at)
);

CREATE TABLE IF NOT EXISTS runbook_links (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  firing_id INTEGER NOT NULL REFERENCES alert_firings(id) ON DELETE CASCADE,
  runbook_key VARCHAR(255) NOT NULL,
  runbook_title VARCHAR(255),
  runbook_url TEXT,
  runbook_version VARCHAR(64),
  metadata_json JSONB NOT NULL DEFAULT '{}'::jsonb,
  created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT runbook_links_runbook_key_not_blank
    CHECK (char_length(btrim(runbook_key)) > 0),
  CONSTRAINT runbook_links_metadata_json_object
    CHECK (jsonb_typeof(metadata_json) = 'object'),
  CONSTRAINT runbook_links_firing_id_runbook_key_unique
    UNIQUE (firing_id, runbook_key)
);

CREATE TABLE IF NOT EXISTS alert_acknowledgements (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  firing_id INTEGER NOT NULL REFERENCES alert_firings(id) ON DELETE CASCADE,
  user_id INTEGER REFERENCES users(id) ON DELETE SET NULL,
  acknowledger VARCHAR(255) NOT NULL,
  note TEXT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT alert_acknowledgements_acknowledger_not_blank
    CHECK (char_length(btrim(acknowledger)) > 0)
);

