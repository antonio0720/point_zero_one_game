CREATE SCHEMA IF NOT EXISTS analytics;

CREATE OR REPLACE FUNCTION set_row_updated_at()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    NEW.updated_at = CURRENT_TIMESTAMP;
    RETURN NEW;
END;
$$;

CREATE TABLE IF NOT EXISTS analytics.run_scorecards (
    run_id TEXT PRIMARY KEY,
    owner_id VARCHAR(64),
    mode_code VARCHAR(32) NOT NULL,
    ruleset_version TEXT,
    outcome VARCHAR(32) NOT NULL,
    grade VARCHAR(2) NOT NULL,
    integrity_status VARCHAR(32) NOT NULL DEFAULT 'PENDING',
    proof_hash VARCHAR(64),
    cord NUMERIC(12, 4) NOT NULL DEFAULT 0,
    decision_speed_score NUMERIC(12, 4) NOT NULL DEFAULT 0,
    shields_maintained_pct NUMERIC(12, 4) NOT NULL DEFAULT 0,
    hater_sabotages_blocked INTEGER NOT NULL DEFAULT 0,
    cascade_chains_broken INTEGER NOT NULL DEFAULT 0,
    pressure_survived_score NUMERIC(12, 4) NOT NULL DEFAULT 0,
    flat_bonus_total NUMERIC(12, 4) NOT NULL DEFAULT 0,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT run_scorecards_run_id_not_blank
        CHECK (char_length(btrim(run_id)) > 0),
    CONSTRAINT run_scorecards_mode_code_valid
        CHECK (
            mode_code IN (
                'GO_ALONE',
                'HEAD_TO_HEAD',
                'TEAM_UP',
                'CHASE_A_LEGEND'
            )
        ),
    CONSTRAINT run_scorecards_outcome_valid
        CHECK (
            outcome IN (
                'FREEDOM',
                'TIMEOUT',
                'BANKRUPT',
                'ABANDONED'
            )
        ),
    CONSTRAINT run_scorecards_grade_valid
        CHECK (grade IN ('S', 'A', 'B', 'C', 'D', 'F')),
    CONSTRAINT run_scorecards_integrity_status_valid
        CHECK (
            integrity_status IN (
                'PENDING',
                'VERIFIED',
                'QUARANTINED',
                'INTEGRITY_VIOLATION',
                'REJECTED'
            )
        ),
    CONSTRAINT run_scorecards_proof_hash_format
        CHECK (
            proof_hash IS NULL
            OR char_length(btrim(proof_hash)) = 64
        ),
    CONSTRAINT run_scorecards_cord_nonnegative
        CHECK (cord >= 0),
    CONSTRAINT run_scorecards_decision_speed_score_nonnegative
        CHECK (decision_speed_score >= 0),
    CONSTRAINT run_scorecards_shields_maintained_pct_nonnegative
        CHECK (shields_maintained_pct >= 0),
    CONSTRAINT run_scorecards_hater_sabotages_blocked_nonnegative
        CHECK (hater_sabotages_blocked >= 0),
    CONSTRAINT run_scorecards_cascade_chains_broken_nonnegative
        CHECK (cascade_chains_broken >= 0),
    CONSTRAINT run_scorecards_pressure_survived_score_nonnegative
        CHECK (pressure_survived_score >= 0)
);

