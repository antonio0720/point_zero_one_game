BEGIN;

CREATE OR REPLACE FUNCTION set_row_updated_at()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
  NEW.updated_at = CURRENT_TIMESTAMP;
  RETURN NEW;
END;
$$;

DO $$
BEGIN
  CREATE TYPE benchmark_status AS ENUM (
    'pending',
    'running',
    'completed',
    'failed',
    'cancelled'
  );
EXCEPTION
  WHEN duplicate_object THEN NULL;
END
$$;

DO $$
BEGIN
  CREATE TYPE benchmark_output_kind AS ENUM (
    'summary',
    'metrics',
    'trace',
    'artifact'
  );
EXCEPTION
  WHEN duplicate_object THEN NULL;
END
$$;

CREATE TABLE IF NOT EXISTS benchmark_scoring_profiles (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(255) NOT NULL,
  description TEXT,
  scoring_config_json JSONB NOT NULL DEFAULT '{}'::jsonb,
  created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT benchmark_scoring_profiles_name_not_blank
    CHECK (char_length(btrim(name)) > 0),
  CONSTRAINT benchmark_scoring_profiles_config_object
    CHECK (jsonb_typeof(scoring_config_json) = 'object')
);

CREATE TABLE IF NOT EXISTS benchmark_runs (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  profile_id INTEGER REFERENCES benchmark_scoring_profiles(id) ON DELETE SET NULL,
  cohort_id INTEGER REFERENCES cohorts(id) ON DELETE SET NULL,
  source_run_id INTEGER REFERENCES runs(id) ON DELETE SET NULL,
  created_by_user_id INTEGER REFERENCES users(id) ON DELETE SET NULL,
  game_mode VARCHAR(32) NOT NULL,
  seed VARCHAR(255),
  status benchmark_status NOT NULL DEFAULT 'pending',
  created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  started_at TIMESTAMPTZ,
  ended_at TIMESTAMPTZ,
  summary_json JSONB NOT NULL DEFAULT '{}'::jsonb,
  metrics_json JSONB NOT NULL DEFAULT '{}'::jsonb,
  CONSTRAINT benchmark_runs_game_mode_valid
    CHECK (
      game_mode IN (
        'GO_ALONE',
        'HEAD_TO_HEAD',
        'TEAM_UP',
        'CHASE_A_LEGEND'
      )
    ),
  CONSTRAINT benchmark_runs_seed_not_blank
    CHECK (seed IS NULL OR char_length(btrim(seed)) > 0),
  CONSTRAINT benchmark_runs_summary_object
    CHECK (jsonb_typeof(summary_json) = 'object'),
  CONSTRAINT benchmark_runs_metrics_object
    CHECK (jsonb_typeof(metrics_json) = 'object'),
  CONSTRAINT benchmark_runs_ended_after_started
    CHECK (
      ended_at IS NULL
      OR started_at IS NULL
      OR ended_at >= started_at
    )
);

CREATE TABLE IF NOT EXISTS benchmark_attempts (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  run_id INTEGER NOT NULL REFERENCES benchmark_runs(id) ON DELETE CASCADE,
  attempt_number INTEGER NOT NULL,
  status benchmark_status NOT NULL DEFAULT 'pending',
  engine_version VARCHAR(128),
  started_at TIMESTAMPTZ,
  ended_at TIMESTAMPTZ,
  output_summary_json JSONB NOT NULL DEFAULT '{}'::jsonb,
  error_json JSONB NOT NULL DEFAULT '{}'::jsonb,
  created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT benchmark_attempts_attempt_number_positive
    CHECK (attempt_number > 0),
  CONSTRAINT benchmark_attempts_output_summary_object
    CHECK (jsonb_typeof(output_summary_json) = 'object'),
  CONSTRAINT benchmark_attempts_error_json_object
    CHECK (jsonb_typeof(error_json) = 'object'),
  CONSTRAINT benchmark_attempts_ended_after_started
    CHECK (
      ended_at IS NULL
      OR started_at IS NULL
      OR ended_at >= started_at
    ),
  CONSTRAINT benchmark_attempts_run_id_attempt_number_unique
    UNIQUE (run_id, attempt_number)
);

CREATE TABLE IF NOT EXISTS benchmark_outputs (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  attempt_id INTEGER NOT NULL REFERENCES benchmark_attempts(id) ON DELETE CASCADE,
  output_kind benchmark_output_kind NOT NULL DEFAULT 'summary',
  output_data JSONB NOT NULL,
  checksum VARCHAR(255),
  created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT benchmark_outputs_output_data_container
    CHECK (jsonb_typeof(output_data) IN ('object', 'array'))
);

CREATE TABLE IF NOT EXISTS benchmark_attempt_run_links (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  attempt_id INTEGER NOT NULL REFERENCES benchmark_attempts(id) ON DELETE CASCADE,
  run_id INTEGER NOT NULL REFERENCES runs(id) ON DELETE CASCADE,
  linked_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT benchmark_attempt_run_links_attempt_id_unique UNIQUE (attempt_id),
  CONSTRAINT benchmark_attempt_run_links_run_id_unique UNIQUE (run_id)
);

CREATE UNIQUE INDEX IF NOT EXISTS benchmark_scoring_profiles_name_uidx
  ON benchmark_scoring_profiles (name);

CREATE INDEX IF NOT EXISTS benchmark_runs_profile_id_idx
  ON benchmark_runs (profile_id);

CREATE INDEX IF NOT EXISTS benchmark_runs_cohort_id_idx
  ON benchmark_runs (cohort_id);

CREATE INDEX IF NOT EXISTS benchmark_runs_source_run_id_idx
  ON benchmark_runs (source_run_id);

CREATE INDEX IF NOT EXISTS benchmark_runs_created_by_user_id_idx
  ON benchmark_runs (created_by_user_id);

CREATE INDEX IF NOT EXISTS benchmark_runs_game_mode_idx
  ON benchmark_runs (game_mode);

CREATE INDEX IF NOT EXISTS benchmark_runs_status_idx
  ON benchmark_runs (status);

CREATE INDEX IF NOT EXISTS benchmark_runs_created_at_idx
  ON benchmark_runs (created_at DESC);

CREATE INDEX IF NOT EXISTS benchmark_runs_metrics_json_gin_idx
  ON benchmark_runs
  USING GIN (metrics_json);

CREATE INDEX IF NOT EXISTS benchmark_attempts_run_id_idx
  ON benchmark_attempts (run_id);

CREATE INDEX IF NOT EXISTS benchmark_attempts_status_idx
  ON benchmark_attempts (status);

CREATE INDEX IF NOT EXISTS benchmark_attempts_started_at_idx
  ON benchmark_attempts (started_at DESC);

CREATE INDEX IF NOT EXISTS benchmark_outputs_attempt_id_idx
  ON benchmark_outputs (attempt_id);

CREATE INDEX IF NOT EXISTS benchmark_outputs_output_kind_idx
  ON benchmark_outputs (output_kind);

CREATE INDEX IF NOT EXISTS benchmark_outputs_output_data_gin_idx
  ON benchmark_outputs
  USING GIN (output_data);

CREATE INDEX IF NOT EXISTS benchmark_attempt_run_links_run_id_idx
  ON benchmark_attempt_run_links (run_id);

DROP TRIGGER IF EXISTS benchmark_scoring_profiles_set_updated_at ON benchmark_scoring_profiles;
CREATE TRIGGER benchmark_scoring_profiles_set_updated_at
BEFORE UPDATE ON benchmark_scoring_profiles
FOR EACH ROW
EXECUTE FUNCTION set_row_updated_at();

DROP TRIGGER IF EXISTS benchmark_attempts_set_updated_at ON benchmark_attempts;
CREATE TRIGGER benchmark_attempts_set_updated_at
BEFORE UPDATE ON benchmark_attempts
FOR EACH ROW
EXECUTE FUNCTION set_row_updated_at();

COMMIT;