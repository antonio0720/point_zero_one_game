-- backend/migrations/2026_02_20_add_creator_profiles.sql
-- Repo-native rewrite: creators extend accounts instead of creating a second auth table.
-- Money is stored in cents to match backend economics style.

BEGIN;

CREATE TABLE IF NOT EXISTS creator_profiles (
    account_id INTEGER PRIMARY KEY REFERENCES accounts(id) ON DELETE CASCADE,
    handle VARCHAR(64) NOT NULL,
    display_name VARCHAR(128),
    bio TEXT,
    contact_email VARCHAR(255),
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT creator_profiles_handle_not_blank
        CHECK (char_length(btrim(handle)) > 0),
    CONSTRAINT creator_profiles_contact_email_not_blank
        CHECK (
            contact_email IS NULL
            OR char_length(btrim(contact_email)) > 0
        )
);

CREATE UNIQUE INDEX IF NOT EXISTS creator_profiles_handle_uidx
    ON creator_profiles ((lower(btrim(handle))));

CREATE UNIQUE INDEX IF NOT EXISTS creator_profiles_contact_email_uidx
    ON creator_profiles ((lower(btrim(contact_email))))
    WHERE contact_email IS NOT NULL;

DROP TRIGGER IF EXISTS creator_profiles_set_updated_at ON creator_profiles;
CREATE TRIGGER creator_profiles_set_updated_at
BEFORE UPDATE ON creator_profiles
FOR EACH ROW
EXECUTE FUNCTION set_row_updated_at();

CREATE TABLE IF NOT EXISTS creator_level_state (
    creator_id INTEGER PRIMARY KEY REFERENCES creator_profiles(account_id) ON DELETE CASCADE,
    level INTEGER NOT NULL DEFAULT 1,
    experience_points BIGINT NOT NULL DEFAULT 0,
    currency_balance_cents BIGINT NOT NULL DEFAULT 0,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT creator_level_state_level_positive
        CHECK (level > 0),
    CONSTRAINT creator_level_state_experience_nonnegative
        CHECK (experience_points >= 0),
    CONSTRAINT creator_level_state_currency_balance_nonnegative
        CHECK (currency_balance_cents >= 0)
);

DROP TRIGGER IF EXISTS creator_level_state_set_updated_at ON creator_level_state;
CREATE TRIGGER creator_level_state_set_updated_at
BEFORE UPDATE ON creator_level_state
FOR EACH ROW
EXECUTE FUNCTION set_row_updated_at();

CREATE TABLE IF NOT EXISTS creator_permissions_cache (
    creator_id INTEGER PRIMARY KEY REFERENCES creator_profiles(account_id) ON DELETE CASCADE,
    permissions JSONB NOT NULL DEFAULT '{}'::jsonb,
    last_computed_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT creator_permissions_cache_permissions_is_object
        CHECK (jsonb_typeof(permissions) = 'object')
);

DROP TRIGGER IF EXISTS creator_permissions_cache_set_updated_at ON creator_permissions_cache;
CREATE TRIGGER creator_permissions_cache_set_updated_at
BEFORE UPDATE ON creator_permissions_cache
FOR EACH ROW
EXECUTE FUNCTION set_row_updated_at();

CREATE TABLE IF NOT EXISTS creator_history_receipts (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    creator_id INTEGER NOT NULL REFERENCES creator_profiles(account_id) ON DELETE CASCADE,
    transaction_type VARCHAR(32) NOT NULL,
    amount_cents BIGINT NOT NULL,
    currency_code CHAR(3) NOT NULL DEFAULT 'USD',
    description TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT creator_history_receipts_transaction_type_valid
        CHECK (transaction_type IN ('income', 'expense')),
    CONSTRAINT creator_history_receipts_amount_nonnegative
        CHECK (amount_cents >= 0),
    CONSTRAINT creator_history_receipts_currency_code_upper
        CHECK (currency_code = upper(currency_code))
);

CREATE INDEX IF NOT EXISTS creator_history_receipts_creator_id_idx
    ON creator_history_receipts (creator_id);

CREATE INDEX IF NOT EXISTS creator_history_receipts_created_at_idx
    ON creator_history_receipts (created_at DESC);

COMMIT;