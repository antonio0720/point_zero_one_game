BEGIN;

CREATE TABLE IF NOT EXISTS institution_entitlements (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    institution_id INTEGER NOT NULL REFERENCES institutions(id) ON DELETE CASCADE,
    entitlement_id BIGINT NOT NULL REFERENCES entitlements(id) ON DELETE CASCADE,
    source_receipt_ledger_id BIGINT REFERENCES receipt_ledger(id) ON DELETE SET NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT institution_entitlements_unique UNIQUE (institution_id, entitlement_id)
);

CREATE TABLE IF NOT EXISTS cohort_entitlements (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cohort_id INTEGER NOT NULL REFERENCES cohorts(id) ON DELETE CASCADE,
    entitlement_id BIGINT NOT NULL REFERENCES entitlements(id) ON DELETE CASCADE,
    source_receipt_ledger_id BIGINT REFERENCES receipt_ledger(id) ON DELETE SET NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT cohort_entitlements_unique UNIQUE (cohort_id, entitlement_id)
);

CREATE TABLE IF NOT EXISTS curriculum_entitlement_receipts (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    receipt_ledger_id BIGINT REFERENCES receipt_ledger(id) ON DELETE SET NULL,
    institution_id INTEGER REFERENCES institutions(id) ON DELETE CASCADE,
    cohort_id INTEGER REFERENCES cohorts(id) ON DELETE CASCADE,
    entitlement_id BIGINT NOT NULL REFERENCES entitlements(id) ON DELETE CASCADE,
    quantity INTEGER NOT NULL,
    grant_scope VARCHAR(32) NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT curriculum_entitlement_receipts_quantity_positive
        CHECK (quantity > 0),
    CONSTRAINT curriculum_entitlement_receipts_scope_valid
        CHECK (grant_scope IN ('INSTITUTION', 'COHORT')),
    CONSTRAINT curriculum_entitlement_receipts_target_valid
        CHECK (
            (grant_scope = 'INSTITUTION' AND institution_id IS NOT NULL)
            OR
            (grant_scope = 'COHORT' AND cohort_id IS NOT NULL)
        )
);

CREATE INDEX IF NOT EXISTS idx_institution_entitlements_institution_id
    ON institution_entitlements (institution_id);

CREATE INDEX IF NOT EXISTS idx_institution_entitlements_entitlement_id
    ON institution_entitlements (entitlement_id);

CREATE INDEX IF NOT EXISTS idx_cohort_entitlements_cohort_id
    ON cohort_entitlements (cohort_id);

CREATE INDEX IF NOT EXISTS idx_cohort_entitlements_entitlement_id
    ON cohort_entitlements (entitlement_id);

CREATE INDEX IF NOT EXISTS idx_curriculum_entitlement_receipts_receipt_ledger_id
    ON curriculum_entitlement_receipts (receipt_ledger_id);

CREATE INDEX IF NOT EXISTS idx_curriculum_entitlement_receipts_institution_id
    ON curriculum_entitlement_receipts (institution_id);

CREATE INDEX IF NOT EXISTS idx_curriculum_entitlement_receipts_cohort_id
    ON curriculum_entitlement_receipts (cohort_id);

CREATE INDEX IF NOT EXISTS idx_curriculum_entitlement_receipts_entitlement_id
    ON curriculum_entitlement_receipts (entitlement_id);

CREATE INDEX IF NOT EXISTS idx_curriculum_entitlement_receipts_created_at
    ON curriculum_entitlement_receipts (created_at DESC);

COMMIT;