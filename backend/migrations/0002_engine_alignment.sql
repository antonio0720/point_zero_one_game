-- ============================================================================
-- POINT ZERO ONE — ENGINE ALIGNMENT SUPPLEMENT
-- 0002_engine_alignment.sql
-- ============================================================================
-- Purpose:
--   Add persistence surfaces that bridge the deployed schema to the live
--   engine contracts in pzo-web/src/engines/
--
--   1. Card mode overlay system (ModeOverlayEngine.ts)
--   2. Card tag weight override tables (CardScorer.ts)
--   3. CORD breakdown / integrity hardening (SovereigntyEngine.ts)
--   4. Run tick checksums (ReplayIntegrityChecker.ts)
--   5. Run proof artifacts (ProofGenerator.ts)
--   6. Verified Run Explorer projection (read model)
--   7. Contract reference tables (runtime ↔ persistence vocabulary)
--   8. Bot profile registry (BotProfileRegistry.ts)
--
-- Depends on: 0001_pzo_sovereign_schema.sql
-- Density6 LLC · Point Zero One · Sovereign Infrastructure
-- ============================================================================

BEGIN;

-- ============================================================================
-- 1. CARD MODE OVERLAY SYSTEM
-- ============================================================================
-- ModeOverlayEngine.ts applies per-mode tag weight multipliers, cost modifiers,
-- targeting overrides, and legality at draw time. These tables persist that
-- configuration so it can be versioned and hot-reloaded.

-- 1.1 Per-card, per-mode legality
CREATE TABLE IF NOT EXISTS game.card_mode_legality (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    card_definition_id  INTEGER NOT NULL REFERENCES game.card_definitions(id) ON DELETE CASCADE,
    game_mode           game_mode NOT NULL,
    is_legal            BOOLEAN NOT NULL DEFAULT TRUE,
    reason              TEXT,
    created_at          TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at          TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT cml_card_mode_unique UNIQUE (card_definition_id, game_mode)
);

CREATE INDEX IF NOT EXISTS cml_mode_legal_idx ON game.card_mode_legality (game_mode, is_legal);
SELECT create_updated_at_trigger('game', 'card_mode_legality');

-- 1.2 Per-card, per-mode runtime overlays
CREATE TABLE IF NOT EXISTS game.card_mode_overlays (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    card_definition_id  INTEGER NOT NULL REFERENCES game.card_definitions(id) ON DELETE CASCADE,
    game_mode           game_mode NOT NULL,
    ruleset_version_id  INTEGER REFERENCES game.ruleset_versions(id) ON DELETE SET NULL,
    cost_multiplier     NUMERIC(8,4) NOT NULL DEFAULT 1.0,
    effect_multiplier   NUMERIC(8,4) NOT NULL DEFAULT 1.0,
    targeting_override  card_targeting,
    decision_timer_ms   INTEGER,
    timing_override     timing_class,
    overlay_json        JSONB NOT NULL DEFAULT '{}'::jsonb,
    is_active           BOOLEAN NOT NULL DEFAULT TRUE,
    created_at          TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at          TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT cmo_overlay_is_object CHECK (jsonb_typeof(overlay_json) = 'object'),
    CONSTRAINT cmo_timer_nonneg CHECK (decision_timer_ms IS NULL OR decision_timer_ms >= 0),
    CONSTRAINT cmo_card_mode_unique UNIQUE (card_definition_id, game_mode)
);

CREATE INDEX IF NOT EXISTS cmo_mode_active_idx ON game.card_mode_overlays (game_mode, is_active);
SELECT create_updated_at_trigger('game', 'card_mode_overlays');

-- 1.3 Per-card, per-mode tag weight overrides
-- ModeOverlayEngine.ts: MODE_TAG_WEIGHTS[mode][tag] * card-specific override
CREATE TABLE IF NOT EXISTS game.card_tag_weight_overrides (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    card_definition_id  INTEGER NOT NULL REFERENCES game.card_definitions(id) ON DELETE CASCADE,
    game_mode           game_mode NOT NULL,
    tag                 VARCHAR(64) NOT NULL,
    weight              NUMERIC(8,4) NOT NULL DEFAULT 1.0,
    created_at          TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT ctw_tag_not_blank CHECK (char_length(btrim(tag)) > 0),
    CONSTRAINT ctw_weight_positive CHECK (weight > 0),
    CONSTRAINT ctw_card_mode_tag_unique UNIQUE (card_definition_id, game_mode, tag)
);

CREATE INDEX IF NOT EXISTS ctw_mode_tag_idx ON game.card_tag_weight_overrides (game_mode, tag);

-- 1.4 Per-card, per-mode timing window permissions
CREATE TABLE IF NOT EXISTS game.card_timing_windows (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    card_definition_id  INTEGER NOT NULL REFERENCES game.card_definitions(id) ON DELETE CASCADE,
    game_mode           game_mode NOT NULL,
    timing              timing_class NOT NULL,
    is_allowed          BOOLEAN NOT NULL DEFAULT TRUE,
    window_duration_ms  INTEGER,
    created_at          TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT ctwin_duration_nonneg CHECK (window_duration_ms IS NULL OR window_duration_ms >= 0),
    CONSTRAINT ctwin_card_mode_timing_unique UNIQUE (card_definition_id, game_mode, timing)
);

CREATE INDEX IF NOT EXISTS ctwin_mode_timing_idx ON game.card_timing_windows (game_mode, timing);

-- ============================================================================
-- 2. CORD BREAKDOWN / INTEGRITY HARDENING
-- ============================================================================
-- SovereigntyEngine.ts completeRun() pipeline:
--   Step 1: ReplayIntegrityChecker.verify()
--   Step 2: ProofGenerator.generate() + buildSignature()
--   Step 3: RunGradeAssigner.computeScore()
-- These tables persist the detailed output of each step.

-- 2.1 CORD score breakdown per run
CREATE TABLE IF NOT EXISTS game.run_cord_breakdowns (
    id                      BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    run_id                  INTEGER NOT NULL REFERENCES game.runs(id) ON DELETE CASCADE,
    decision_speed_score    NUMERIC(12,6) NOT NULL DEFAULT 0,
    shields_maintained_pct  NUMERIC(12,6) NOT NULL DEFAULT 0,
    hater_sabotages_blocked INTEGER NOT NULL DEFAULT 0,
    cascade_chains_broken   INTEGER NOT NULL DEFAULT 0,
    pressure_survived_score NUMERIC(12,6) NOT NULL DEFAULT 0,
    outcome_multiplier      NUMERIC(8,4) NOT NULL DEFAULT 1.0,
    mode_multiplier         NUMERIC(8,4) NOT NULL DEFAULT 1.0,
    mode_bonus_json         JSONB NOT NULL DEFAULT '{}'::jsonb,
    raw_cord                NUMERIC(18,6) NOT NULL DEFAULT 0,
    final_cord              NUMERIC(18,6) NOT NULL DEFAULT 0,
    created_at              TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at              TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT rcb_mode_bonus_is_object CHECK (jsonb_typeof(mode_bonus_json) = 'object'),
    CONSTRAINT rcb_raw_cord_nonneg CHECK (raw_cord >= 0),
    CONSTRAINT rcb_final_cord_nonneg CHECK (final_cord >= 0),
    CONSTRAINT rcb_run_unique UNIQUE (run_id)
);

CREATE INDEX IF NOT EXISTS rcb_run_idx ON game.run_cord_breakdowns (run_id);
CREATE INDEX IF NOT EXISTS rcb_final_cord_idx ON game.run_cord_breakdowns (final_cord DESC);
SELECT create_updated_at_trigger('game', 'run_cord_breakdowns');

-- 2.2 Tick stream checksums (ReplayIntegrityChecker)
CREATE TABLE IF NOT EXISTS game.run_tick_checksums (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    run_id              INTEGER NOT NULL REFERENCES game.runs(id) ON DELETE CASCADE,
    first_tick          INTEGER NOT NULL,
    last_tick           INTEGER NOT NULL,
    tick_count          INTEGER NOT NULL,
    checksum_sha256     VARCHAR(64) NOT NULL,
    source              VARCHAR(32) NOT NULL DEFAULT 'engine',
    created_at          TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT rtc_first_tick_nonneg CHECK (first_tick >= 0),
    CONSTRAINT rtc_last_tick_nonneg CHECK (last_tick >= 0),
    CONSTRAINT rtc_tick_count_positive CHECK (tick_count > 0),
    CONSTRAINT rtc_range_valid CHECK (last_tick >= first_tick),
    CONSTRAINT rtc_checksum_format CHECK (char_length(btrim(checksum_sha256)) = 64),
    CONSTRAINT rtc_run_range_unique UNIQUE (run_id, first_tick, last_tick)
);

CREATE INDEX IF NOT EXISTS rtc_run_idx ON game.run_tick_checksums (run_id);

-- 2.3 Proof artifacts (ProofGenerator)
CREATE TABLE IF NOT EXISTS game.run_proof_artifacts (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    run_id              INTEGER NOT NULL REFERENCES game.runs(id) ON DELETE CASCADE,
    proof_hash          VARCHAR(64) NOT NULL,
    artifact_kind       VARCHAR(32) NOT NULL,
    storage_url         VARCHAR(512),
    render_status       VARCHAR(32) NOT NULL DEFAULT 'PENDING',
    border_style        VARCHAR(32),
    metadata_json       JSONB NOT NULL DEFAULT '{}'::jsonb,
    created_at          TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at          TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT rpa_proof_hash_format CHECK (char_length(btrim(proof_hash)) = 64),
    CONSTRAINT rpa_kind_not_blank CHECK (char_length(btrim(artifact_kind)) > 0),
    CONSTRAINT rpa_render_status_valid CHECK (render_status IN ('PENDING','RENDERING','READY','FAILED')),
    CONSTRAINT rpa_metadata_is_object CHECK (jsonb_typeof(metadata_json) = 'object')
);

CREATE INDEX IF NOT EXISTS rpa_run_idx ON game.run_proof_artifacts (run_id);
CREATE INDEX IF NOT EXISTS rpa_kind_idx ON game.run_proof_artifacts (artifact_kind);
CREATE INDEX IF NOT EXISTS rpa_proof_hash_idx ON game.run_proof_artifacts (proof_hash);
SELECT create_updated_at_trigger('game', 'run_proof_artifacts');

-- 2.4 Integrity review queue
CREATE TABLE IF NOT EXISTS game.run_integrity_reviews (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    run_id              INTEGER NOT NULL REFERENCES game.runs(id) ON DELETE CASCADE,
    verdict             verification_status NOT NULL,
    reviewer_kind       VARCHAR(32) NOT NULL DEFAULT 'SYSTEM',
    proof_hash_at_review VARCHAR(64),
    tick_checksum_at_review VARCHAR(64),
    review_notes        TEXT,
    evidence_json       JSONB NOT NULL DEFAULT '{}'::jsonb,
    created_at          TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT rir_evidence_is_object CHECK (jsonb_typeof(evidence_json) = 'object')
);

CREATE INDEX IF NOT EXISTS rir_run_idx ON game.run_integrity_reviews (run_id, created_at DESC);
CREATE INDEX IF NOT EXISTS rir_verdict_idx ON game.run_integrity_reviews (verdict);

