-- 4. VERIFIED RUN EXPLORER (READ MODEL)
-- ============================================================================
-- Denormalized projection fed by SovereigntyEngine on RUN_COMPLETED.
-- The Verified Run Explorer queries this, not the operational runs table.

CREATE TABLE IF NOT EXISTS analytics.verified_run_explorer (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    run_id              INTEGER NOT NULL REFERENCES game.runs(id) ON DELETE CASCADE,
    user_id             INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    game_mode           game_mode NOT NULL,
    verification        verification_status NOT NULL,
    grade               run_grade,
    cord                NUMERIC(18,6) NOT NULL DEFAULT 0,
    outcome             run_outcome,
    proof_hash          VARCHAR(64),
    season_key          VARCHAR(32),
    is_public           BOOLEAN NOT NULL DEFAULT FALSE,
    filter_tags         TEXT[] NOT NULL DEFAULT '{}',
    highlight_json      JSONB NOT NULL DEFAULT '{}'::jsonb,
    completed_at        TIMESTAMPTZ,
    created_at          TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at          TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT vre_highlight_is_object CHECK (jsonb_typeof(highlight_json) = 'object'),
    CONSTRAINT vre_cord_nonneg CHECK (cord >= 0),
    CONSTRAINT vre_run_unique UNIQUE (run_id)
