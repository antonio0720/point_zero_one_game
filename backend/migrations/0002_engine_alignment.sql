-- 7. ENGINE STATE PERSISTENCE VIEWS
-- ============================================================================
-- Convenience views for common engine queries. PgBouncer-safe (no SET).

-- Active runs per mode (for matchmaking / capacity)
CREATE OR REPLACE VIEW game.v_active_runs AS
SELECT
    game_mode,
    COUNT(*) AS active_count,
    MIN(started_at) AS oldest_start,
    MAX(total_ticks) AS max_ticks
FROM game.runs
WHERE status IN ('RUNNING','LOADING','PAUSED')
GROUP BY game_mode;

-- Leaderboard-ready verified runs
CREATE OR REPLACE VIEW analytics.v_leaderboard_verified AS
SELECT
    r.id AS run_id,
    r.user_id,
    u.handle,
    r.game_mode,
    r.cord_final,
    r.grade,
    r.outcome,
    r.proof_hash,
    r.is_bleed_mode,
    r.completed_at
FROM game.runs r
JOIN users u ON u.id = r.user_id
WHERE r.verification = 'VERIFIED'
  AND r.outcome = 'FREEDOM'
  AND r.cord_final > 0
ORDER BY r.cord_final DESC;

-- Bot activity summary per run
CREATE OR REPLACE VIEW game.v_run_bot_summary AS
SELECT
    run_id,
    bot,
    MAX(heat) AS peak_heat,
    COUNT(*) FILTER (WHERE state = 'ATTACKING') AS attack_ticks,
    COUNT(*) FILTER (WHERE state = 'NEUTRALIZED') AS neutralized_ticks,
    MAX(tick) AS last_tick
FROM game.run_bot_states
GROUP BY run_id, bot;

