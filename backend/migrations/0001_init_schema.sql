    WHERE deleted_at IS NULL;

CREATE INDEX IF NOT EXISTS device_trust_deleted_at_idx
    ON device_trust (deleted_at);

CREATE INDEX IF NOT EXISTS profiles_user_id_idx
    ON profiles (user_id);

CREATE UNIQUE INDEX IF NOT EXISTS profiles_profile_name_active_uidx
    ON profiles (profile_name)
    WHERE deleted_at IS NULL;

CREATE INDEX IF NOT EXISTS profiles_deleted_at_idx
    ON profiles (deleted_at);

CREATE INDEX IF NOT EXISTS account_binds_user_id_idx
    ON account_binds (user_id);

CREATE INDEX IF NOT EXISTS account_binds_account_type_idx
    ON account_binds (account_type);

CREATE UNIQUE INDEX IF NOT EXISTS account_binds_account_type_account_id_active_uidx
    ON account_binds (account_type, account_id)
    WHERE deleted_at IS NULL;

CREATE INDEX IF NOT EXISTS account_binds_deleted_at_idx
    ON account_binds (deleted_at);

CREATE INDEX IF NOT EXISTS refresh_tokens_account_bind_id_idx
    ON refresh_tokens (account_bind_id);

CREATE UNIQUE INDEX IF NOT EXISTS refresh_tokens_token_active_uidx
    ON refresh_tokens (token)
    WHERE deleted_at IS NULL;

