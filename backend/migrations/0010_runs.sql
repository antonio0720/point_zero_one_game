CREATE TABLE IF NOT EXISTS runs (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    session_id INTEGER NOT NULL REFERENCES sessions(id) ON DELETE CASCADE,
    seed VARCHAR(255) NOT NULL,
    game_mode VARCHAR(32) NOT NULL,
    status run_status NOT NULL DEFAULT 'IDLE',
    visibility_mode run_visibility_mode NOT NULL DEFAULT 'HIDDEN',
    verification_status run_verification_status NOT NULL DEFAULT 'PENDING',
    is_quarantined BOOLEAN NOT NULL DEFAULT FALSE,
    started_at TIMESTAMPTZ,
    completed_at TIMESTAMPTZ,
    quarantined_at TIMESTAMPTZ,
    summary_json JSONB NOT NULL DEFAULT '{}'::jsonb,
    score_json JSONB NOT NULL DEFAULT '{}'::jsonb,
    public_redaction_json JSONB NOT NULL DEFAULT '{}'::jsonb,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT runs_seed_not_blank
        CHECK (char_length(btrim(seed)) > 0),
    CONSTRAINT runs_game_mode_valid
        CHECK (
            game_mode IN (
                'GO_ALONE',
                'HEAD_TO_HEAD',
                'TEAM_UP',
                'CHASE_A_LEGEND'
            )
        ),
    CONSTRAINT runs_summary_json_object
        CHECK (jsonb_typeof(summary_json) = 'object'),
    CONSTRAINT runs_score_json_object
        CHECK (jsonb_typeof(score_json) = 'object'),
    CONSTRAINT runs_public_redaction_json_object
        CHECK (jsonb_typeof(public_redaction_json) = 'object'),
    CONSTRAINT runs_completed_after_started
        CHECK (
            completed_at IS NULL
            OR started_at IS NULL
            OR completed_at >= started_at
        ),
    CONSTRAINT runs_quarantined_requires_flag
        CHECK (
            quarantined_at IS NULL
            OR is_quarantined = TRUE
        )
);

CREATE TABLE IF NOT EXISTS run_turns (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    run_id INTEGER NOT NULL REFERENCES runs(id) ON DELETE CASCADE,
    turn INTEGER NOT NULL,
    phase VARCHAR(32),
    state_json JSONB NOT NULL DEFAULT '{}'::jsonb,
    metrics_json JSONB NOT NULL DEFAULT '{}'::jsonb,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT run_turns_turn_nonnegative
        CHECK (turn >= 0),
    CONSTRAINT run_turns_state_json_object
        CHECK (jsonb_typeof(state_json) = 'object'),
    CONSTRAINT run_turns_metrics_json_object
        CHECK (jsonb_typeof(metrics_json) = 'object'),
    CONSTRAINT run_turns_run_id_turn_unique
        UNIQUE (run_id, turn)
);

CREATE TABLE IF NOT EXISTS run_events (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    run_id INTEGER NOT NULL REFERENCES runs(id) ON DELETE CASCADE,
    turn_id INTEGER REFERENCES run_turns(id) ON DELETE SET NULL,
    global_event_store_id BIGINT REFERENCES global_event_store(id) ON DELETE SET NULL,
    event_seq INTEGER NOT NULL,
    event_name VARCHAR(128) NOT NULL,
    payload_json JSONB NOT NULL DEFAULT '{}'::jsonb,
    public_visibility run_visibility_mode NOT NULL DEFAULT 'HIDDEN',
    redaction_json JSONB NOT NULL DEFAULT '{}'::jsonb,
    occurred_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT run_events_event_seq_positive
        CHECK (event_seq > 0),
    CONSTRAINT run_events_event_name_not_blank
        CHECK (char_length(btrim(event_name)) > 0),
    CONSTRAINT run_events_payload_json_object
        CHECK (jsonb_typeof(payload_json) = 'object'),
    CONSTRAINT run_events_redaction_json_object
        CHECK (jsonb_typeof(redaction_json) = 'object'),
    CONSTRAINT run_events_run_id_event_seq_unique
        UNIQUE (run_id, event_seq)
);

